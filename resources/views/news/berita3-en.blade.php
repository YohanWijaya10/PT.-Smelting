                <p>
                    Another hobby club has been formed within the PT Smelting (PTS) environment. Its name is the Smelting Running Club.
                    As the name suggests, this is a club for running enthusiasts.

                    When was this club formed? Who are its members? When is the training? How can one become a
                    club member? What are the benefits? Here is an excerpt from an interview with Smelting Magazine about it.
                    <br>
                    <br>
                    The Smelting Running Club officially started running on October 6, 2023. Actually, since March 2023,
                    some PTS employees who started or were engaged in running activities began to gather, especially
                    when participating in several Fun-Run or Race events in Surabaya and its surroundings.
                    At each event, the number of PTS employees participating increased. Finally, on October 6, 2023, the
                    Smelting Running Club was formed, marked by the creation of a WhatsApp group. In that WhatsApp group,
                    joint running training events were initiated, information about Running Events was shared, and tips
                    on running or running gear were shared.

                    <br>
                    <br>

                    Meanwhile, members of the Smelting Running Club are PTS employees from various sections such as Technical,
                    Laboratory, Smelter, Refinery, Acid & WWTP, Marketing & Sales, Logistics, Accounting, and Purchasing.
                    Most of the Smelting Running Club members run to stay healthy and meet the recommendations of their
                    annual Medical Check-Up results. Only a few members initially ran as a hobby. Therefore, if you look
                    at the group photo of the Smelting Running Club members, approximately only 25% of the members have
                    a runner's body posture, hehehehe.

                    <br>
                    <br>
                    The management of the Smelting Running Club was immediately formed on October 6, 2023. The chairman is
                    M Syaifudin (Smelter), Secretary Zakiyatmoko B (Acid Plant & WWTP), and treasurer Yosafat Pinandito
                    (Purchasing & Warehouse).

                    <br>
                    <br>

                    We haven't set a regular schedule. This is because of various busyness and clashes with other
                    hobby activities and the operational conditions of the factory. But it is planned to conduct joint
                    running activities with general menus such as Easy Run or Long Run every 2 weeks on Saturdays or Sundays.
                    To avoid boredom, the running locations will be rotated between places in Gresik and Surabaya.

                    <br>
                    <br>

                    Until now, if there is a joint training activity, we announce it in the PTS employee WhatsApp group.
                    So other employees who want to join can come directly. But generally, those who join are invited
                    personally. So, if Smelting Running Club members find out that other employees are starting to run,
                    have a running hobby, or are just starting to intend to run, they will immediately invite them to join
                    the Smelting Running Club.

                    <br>
                    <br>

                    Since October 2022, the Smelting Running Club has participated in several Running Events in Surabaya
                    such as Road to Give-Marriott Hotel (October 2022), Run for Sanity ‚Äì FK UNAIR (March 2023), Run Hub
                    (October 8, 2023), Airlangga Run (November 19, 2023), IPPT Patriot Run (December 3, 2023), and KIP Night
                    Run (December 9, 2023). Running activities have also been carried out outside the city, namely in Yogyakarta,
                    such as Malioboro Run Yogyakarta (October 29, 2023), and Borobudur Marathon Yogyakarta (November 19, 2023).
                    In addition, joint training with the Gresik Easy-Run 8 Km menu has also been held on November 11, 2023.

                    <br>
                    <br>

                    Running is the most effective sport for burning body calories. Running is also the easiest sport to do
                    because it doesn't require special equipment, can be done independently, doesn't need to rent a place,
                    and can be done in many places and anytime. One way to make running more enthusiastic is to do it together.
                    For PTS employees who have a hobby, just starting, or even just intending to run, you can join the Smelting
                    Running Club to make the running experience more enjoyable. "No matter how far or how fast you run, when
                    you're running, You're Runners."

                    <br>
                    <br>

                    We plan to make joint training routine with various running training menus. We also want to further
                    facilitate or at least help our fellow Smelting Runners in participating in various Running Events. (Mitha/SM)
                </p>
