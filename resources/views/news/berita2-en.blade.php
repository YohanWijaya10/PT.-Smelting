                    <p>
                        The scorching sun did not deter President Joko Widodo (Jokowi) and several ministers from admiring the copper smelter PT Smelting (PTS) in Gresik. The VVIP (Very-Very Important Person) guests appeared serious as they listened to explanations about the smelter's operations from PTS President Director Hideya Sato (Sato).
        
                        Thursday, December 14, 2023, was indeed a historic day for PTS. For the first time since its establishment in 1996, the copper concentrate processor from PT Freeport Indonesia's Timika-Papua mine was visited by the President of the Republic of Indonesia. President Jokowi attended to inaugurate PTS's fourth expansion project.
        
                        President Jokowi arrived at PTS at 3:30 PM. He was welcomed by Sato along with President Director of PT Freeport Indonesia (PTFI) Tony Wenas. Minister of State-Owned Enterprises Erick Thohir was also present, while Minister of Investment Bahlil Lahadalla was seen accompanying President Jokowi.
        
                        After getting off the presidential car, President Jokowi boarded a golf cart for a factory tour. President Jokowi and his entourage descended from the golf cart right in front of the new smelter to be inaugurated. Along the way to this location, dozens of PTS employees welcomed them, lining up on the side of the road with small red and white flags.
        
                        Meanwhile, next to the new smelter building, a large tent was set up for the inauguration ceremony. Chief Executive Officer of Mitsubishi Materials Corporation (MMC), Naoki Ono (Ono), came specifically from Tokyo, Japan, and Freeport McMoran (FCX) Chairman Mr. Richard Adkerson from the United States attended the event.
        
                        There were four MMC executives who came directly from Tokyo besides Mr. Ono. They were Managing Executive Officer & President of Metals Company MMC Katsuyoshi Isaji, General Manager of the Metallurgy Division of Metals Company MMC Teruyuki Matsutani, General Manager of Planning & Administration Department of Metals Company MMC Munenari Narazaki, and AGM of Planning & Administration Department of Metals Company MMC Atsushi Todo.
                        <br>
                        <br>
                        President Jokowi and the entourage stopped in front of the new smelter to get an explanation about the operational processing of copper cathodes from Mr. Sato. Mr. Sato explained it in Indonesian. His explanation could also be heard through the sound system at the event location.
        
                        After that, President Jokowi took the opportunity to inspect the environmentally friendly MMC technology smelter. He climbed the factory stairs next to the event tent. He was accompanied by several presidential guards and ministers. East Java Governor Khofifah Indar Parawansa was also seen.
        
                        The direct factory tour took about half an hour. It is unknown what President Jokowi asked the top management of PTS. However, after completing the smelter tour, President Jokowi seemed cheerful as he walked under the scorching sun towards the inauguration ceremony venue.
                        <br>
                        <br>
                        This fourth expansion of PTS's smelter will increase production capacity from 1 million tons of concentrate to 1.3 million tons. The investment for this new smelter expansion is funded by PTFI.
        
                        After the tour was completed, the inauguration ceremony of PTS's fourth expansion began with a speech from Minister of State-Owned Enterprises Erick Thohir (Erick). He stated that this inauguration is evidence of the government's seriousness in implementing downstreaming.
        
                        "Although there are those who do not understand the urgency of the downstreaming policy, the steps taken by President Jokowi are the right steps and a reality that must happen," he said.
        
                        Erick added that Indonesia is a large country with abundant mineral resources. Downstreaming policies are a commitment so that the nation is no longer exploited by other nations.
        
                        According to the minister, who is also the Chairman of the Indonesian Football Association (PSSI), Indonesia cannot continuously sell raw minerals without sustainable benefits, such as job creation and technology transfer.
                        <br>
                        <br>
                        "If there are parties protesting downstreaming and wanting us to continue selling raw mineral materials, I think their nationalism needs to be questioned," said Erick.
        
                        The expansion of the PTS plant is not only significant for FCX and MMC but also for the Indonesian government. President Jokowi, in his speech, appreciated PTS's expansion, which can increase the production capacity of copper anodes and cathodes and add value in Indonesia.
        
                        "The production of copper anodes and cathodes has increased from 1 million tons per year to 1.3 million tons per year. This shows the commitment of PTFI, which collaborates with MMC to downstream, build downstream with expansion so that the added value is in Indonesia," said President Jokowi.
        
                        President Jokowi hopes that PTS's expansion will increase production capacity. With all of this, Indonesia will gain added value.
        
                        President Jokowi encourages downstreaming not only in mineral materials but also in plantation and agriculture commodities. According to President Jokowi, downstreaming can increase job opportunities and added value in Indonesia.
                        <br>
                        <br>
                        "Everything about downstreaming adds value to our country, job opportunities are in our country, whether it's in plantations, agriculture, fisheries, everything. Don't let us send minerals in raw form, plantation materials, plantation commodities, agriculture in raw form," emphasized President Jokowi.
        
                        After the ceremony, President Jokowi took the time to speak with journalists outside the tent. At that time, he emphasized that downstreaming efforts in every commodity in Indonesia aim to increase added value to strengthen national economic growth.
        
                        "Once again, the added value from this expansion will make this country a producer, a production country, and the strength of economic growth is in production, not in consumption," said President Jokowi.
        
                        Not only did he serve journalists, but President Jokowi also took the time to fulfill the requests of PTS employees for a group photo. This impressed the employees. They also have a group photo with President Jokowi and PTS leaders after the inauguration.
        
                        Indeed, this is a historic end of the year for PTS and all its leaders and employees. (Mitha and SM Team)
