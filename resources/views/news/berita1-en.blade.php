                    <p>PT Smelting (PTS) once again achieved the Green PROPER in 2023 in the Performance Rating Program for Company Environmental Management (PROPER) awards organized by the Ministry of Environment and Forestry (KLHK).
                        PROPER assessment is divided into two categories: compliance with environmental regulations and criteria beyond compliance. The compliance rating criteria include Black, Red, and Blue ratings. Meanwhile, for companies that exceed the assessment criteria (Beyond Compliance), there are two ratings: Green and Gold.
                        In Ministerial Decree No. SK.1353/MENLHK/SETJEN/KUM.1/12/2023, dated December 15, 2023, 79 companies received Gold ratings, 196 companies received Green ratings, 2,131 companies received Blue ratings, and 1,077 companies received Red ratings.
                        <br>
                        <br>
                        The aspects assessed in the Beyond Compliance criteria include Life Cycle Assessment (LCA), Environmental Management System (EMS), Energy Efficiency, Emission Reduction, 3R Hazardous Waste, 3R Non-Hazardous Waste, Water Efficiency and Pollution Load Reduction, Biodiversity, Community Empowerment, and Disaster Response.
                        The attainment of this Green PROPER indicates that PTS is consistent in its environmental management efforts, including preserving biodiversity and contributing to the surrounding community. This aligns with PTS management's commitment to implementing sustainable and responsible production activities.
                        The award was presented by the Deputy Minister of the Ministry of Environment and Forestry, Alue Dohong, and was received directly by PTS Director of Operations and Engineering, Osamu Inoue, at the Bidakara Hotel, Jakarta, on December 20, 2023.
                        Osamu Inoue expressed, "It is an honor and a remarkable impression for me to participate in PROPER AWARD 2023 as a representative of PTS. PTS's dedication to environmental management and its contribution to the wider community beyond compliance are recognized and acknowledged."
                        <br>
                        <br>
                        The process of filling out the green PROPER documents in 2023 is different from previous years. Previously, green document files were uploaded to Google Drive with a maximum size of 3GB. However, in 2023, the green document filling is done in the KLHK SIMPEL application, with a total attachment size uploaded to the SIMPEL system being 2GB, reduced from the previous year.
                        The filling deadline for green documents is also different this time, with each company category having a different deadline. The filling time frame is from October 12-15, 2023. PTS, categorized as Metal and Mineral Processing, had a filling deadline on October 14, 2023. Since this system is new, it presented a challenge, but the PTS team successfully filled it out without any issues.
                        PTS has made various efforts, including conducting a Life Cycle Assessment (LCA) study. The PTS LCA assessment uses the Cradle to Grave system, starting from copper cathode production activities to the return of the final product to nature or disposal. (Marwa/SM).</p>
