                        <br>

                        Kami belum menentukan jadwal rutin. Hal ini karena berbagai kesibukan dan bentrok dengan
                        aktifitas hobi yang lain serta kondisi operasional pabrik. Tapi direncanakan akan dilakukan
                        kegiatan berlari bersama dengan menu umumnya Easy Run atau Long Run setiap 2 minggu sekali pada
                        hari Sabtu atau Minggu. Agar tidak bosan, tempat berlari akan dirotasi antara tempat-tempat di
                        Gresik dan Surabaya.

                        <br>
                        <br>

                        Sampai saat ini, jika ada kegiatan latihan bersama kami umumkan di WA grup karyawan PTS. Jadi
                        para karyawan lain yang ingin bergabung bisa langsung datang. Tapi umumnya yang bergabung itu
                        berasal dari ajakan personal. Jadi para anggota Smelting Running Club jika mengetahui ada
                        karyawan lain yang mulai berlari, hobi berlari atau baru niat berlari akan langsung diajak masuk
                        ke Smelting Running Club.

                        <br>
                        <br>

                        Terhitung sejak bulan Oktober 2022, Smelting Running Club sudah mengikuti beberapa Running
                        Events di Surabaya seperti Road to Give-Marriot Hotel (Oktober 2022), Run for Sanity – FK UNAIR
                        (Maret 2023), Run Hub (8 Oktober 2023), Airlangga Run (19 November 2023), IPPT Patriot Run (3
                        Desember 2023) dan KIP Night Run (9 Desember 2023). Adapun kegiatan lari juga dilakukan di luar
                        kota yaitu Yogyakarta, seperti Malioboro Run Yogyakarta (29 Oktober 2023), dan Borobudur
                        Marathon Yogyakarta (19 November 2023). Selain itu, latihan bersama dengan menu Gresik Easy-Run
                        8 Km juga telah dilaksanakan pada 11 November 2023.

                        <br>
                        <br>

                        Berlari merupakan olah raga yang paling ampuh untuk membakar kalori tubuh. Berlari juga
                        merupakan olah raga yang paling mudah dilakukan karena tidak perlu alat-alat khusus, dapat
                        dilakukan secara mandiri, tidak perlu sewa tempat dan dapat dilakukan di banyak tempat serta
                        kapan pun waktunya. Salah satu cara agar berlari menjadi semakin semangat adalah dengan
                        dilakukan secara bersama-sama. Untuk karyawan PTS yang memiliki hobi, baru memulai atau bahkan
                        baru berniat berlari dapat bergabung dengan Smelting Running Club agar pengalaman berlari lebih
                        menyenangkan. “Tidak peduli seberapa jauh atau seberapa cepat kau berlari, ketika kau sudah
                        berlari You’re Runners.”

