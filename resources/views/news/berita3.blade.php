                        <br>
                        <br>
                        Smelting Running Club secara resmi mulai berjalan sejak 6 Oktober 2023. Sebetulnya sejak bulan
                        Maret 2023, beberapa karyawan PTS yang mulai atau sedang menekuni kegiatan berlari sudah mulai
                        berkumpul terutama saat mengikuti beberapa event Fun-Run atau Race di Surabaya dan sekitarnya.
                        Setiap event tersebut, jumlah karyawan PTS yang ikut semakin meningkat. Akhirnya pada 6 Oktober
                        2023 dibentuklah Smelting Running Club yang ditandai dengan dibuatnya grup WA. Dan di grup WA
                        itu mulai diinisiasi acara latihan lari bersama, berbagi informasi Running Events serta berbagi
                        tips berlari atau running gear.

                        <br>
                        <br>

                        Sementara ini, anggota Smelting Running Club adalah para karyawan PTS dari berbagai section
                        seperti Technical, Laboratory, Smelter, Refinery, Acid & WWTP, Marketing & Sales, Logistic,
                        Accounting dan Purchasing. Mayoritas para anggota Smelting Running Club berlari karena keinginan
                        untuk lebih sehat dan memenuhi rekomendasi hasil Medical Check-Up tahunan. Hanya beberapa
                        anggota saja yang awalnya memang sebagai pelari atau hobi berlari. Karena itu, jika dilihat dari
                        foto bersama anggota Smelting Running Club, kurang lebih hanya 25% anggota yang memiliki postur
                        tubuh pelari hehehehe.

