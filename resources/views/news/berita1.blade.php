                        file dokumen hijau diupload di Google Drive dengan maksimal ukuran 3GB. Namun pada 2023, pengisian
                        dokumen hijau dilakukan di aplikasi SIMPEL KLHK dengan total ukuran lampiran yang diupload pada
                        sistem SIMPEL adalah 2GB sehingga berkurang dari tahun lalu.
    
                        Batas pengisian dokumen hijau kali ini juga berbeda dengan sebelumnya yaitu tiap kategori perusahaan
                        memiliki batas pengisian yang berbeda. Rentang batas waktu pengisian adalah dari tanggal 12-15
                        Oktober 2023. PTS yang masuk dalam kategori Pengolahan Logam dan Mineral mendapatkan batas pengisian
                        pada tanggal 14 Oktober 2023. Karena sistem ini merupakan hal baru sehingga cukup menantang, namun
                        tim PTS berhasil melakukan pengisian dengan baik dan tidak terkendala.
    
                        Berbagai upaya dilakukan oleh PTS, salah satunya adalah melakukan kajian penilaian daur hidup atau
                        disebut Life Cycle Analysis (LCA). Kajian penilaian LCA PTS menggunakan batasan sistem Cradle to
                        Grave, mulai dari kegiatan produksi katoda tembaga sampai ke pengembalian produk akhir ke alam atau
