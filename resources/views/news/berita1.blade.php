                    <p>PT Smelting (PTS) kembali meraih PROPER Hijau di tahun 2023 pada penghargaan Program Penilaian
                        Peringkat Kinerja Perusahaan dalam Pengelolaan Lingkungan (PROPER) yang diselenggarakan oleh
                        Kementerian Lingkungan Hidup dan Kehutanan (KLHK).
                        Penilaian PROPER dibagi menjadi 2 kategori yaitu kriteria penilaian ketaatan terhadap lingkungan dan
                        kriteria penilaian lebih dari yang dipersyaratkan dalam peraturan (Beyond Compliance). Tingkatan
                        peringkat untuk kriteria ketaatan terhadap peraturan lingkungan adalah peringkat Hitam, Merah, dan
                        Biru. Sedangkan untuk perusahaan yang memenuhi kriteria penilaian yang lebih dari yang
                        dipersyaratkan (Beyond Compliance) terdiri atas dua peringkat, yakni Hijau dan Emas.
                        Dalam Keputusan Menteri LHK No. SK.1353/MENLHK/SETJEN/KUM.1/12/2023 yang ditetapkan pada 15 Desember
                        2023, ada 79 perusahaan dengan peringkat Emas, 196 perusahaan dengan peringkat Hijau, untuk
                        peringkat biru sebanyak 2.131 perusahaan, dan peringkat merah sebanyak 1.077 perusahaan.
                        <br>
                        <br>
                        Aspek-aspek yang dinilai dalam kriteria Beyond Compliance adalah LCA (Life Cycle Assessment), SML
                        (Sistem Manajemen Lingkungan), Efisiensi Energi, Penurunan Emisi, 3R Limbah B3, 3R Limbah Non B3,
                        Efisiensi Air dan Penurunan Beban Pencemaran, Keanekaragaman Hayati, Pemberdayaan Masyarakat, dan
                        Tanggap Kebencanaan.
    
                        Dengan didapatkannya PROPER Hijau ini menunjukkan bahwa PTS konsisten dalam melakukan upaya
                        pengelolaan lingkungannya termasuk melestarikan keanekaragaman hayati dan memberikan kontribusi
                        kepada masyarakat sekitar. Hal tersebut searah dengan komitmen manajemen PTS untuk menerapkan
                        kegiatan produksi yang berkelanjutan dan bertanggung jawab terhadap lingkungan.
    
                        Penghargaan tersebut diberikan oleh Wakil Menteri Kementerian Lingkungan Hidup dan Kehutanan, Alue
                        Dohong, dan diterima langsung oleh Direktur Operasional dan Teknik PTS, Osamu Inoue, di Hotel
                        Bidakara, Jakarta pada tanggal 20 Desember 2023.
    
                        Osamu Inoue mengungkapkan bahwa ‚ÄúSuatu kehormatan dan kesan yang luar biasa bagi saya untuk
                        mengikuti PROPER AWARD 2023 sebagai perwakilan PTS. Dedikasi PTS terhadap pengelolaan lingkungan
                        hidup serta kontribusi PTS kepada masyarakat luas yang lebih dari yang dipersyarakatkan (Beyond
                        Compliance) ini diakui dan dikenal.‚Äù
