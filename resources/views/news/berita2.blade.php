                    <p> 
                        Panas matahari yang menyengat tak menghalangi Presiden Joko Widodo (Jokowi) dan sejumlah menteri
                        mengagumi smelter tembaga PT Smelting (PTS) di Gresik. Para tamu VVIP (Very-Very Important
                        Person) itu tampak serius mendengarkan penjelasan tentang operasional smelter tersebut dari
                        Presiden Direktur PTS Hideya Sato (Sato).

                        Hari itu, Kamis, 14 Desember 2023, memang hari bersejarah bagi PTS. Untuk kali pertama sejak
                        berdiri tahun 1996, pengolah konsentrat tembaga hasil tambang PT Freeport Indonesia di
                        Timika-Papua itu dikunjungi Presiden Republik Indonesia. Presiden Jokowi hadir untuk meresmikan
                        proyek ekspansi keempat PTS.

                        Presiden Jokowi tiba di PTS pukul 15.30. Ia langsung disambut oleh Sato bersama Presiden
                        Direktur PT Freeport Indonesia (PTFI) Tony Wenas. Tampak juga Menteri BUMN Erick Thohir.
                        Sedangkan Menteri Investasi Bahlil Lahadalla tampak ikut dalam rombongan Presiden Jokowi.
                        <br>
                        <br>
                        Setelah turun dari mobil kepresidenan, Presiden Jokowi menaiki golfcar untuk peninjauan pabrik.
                        Presiden Jokowi dan rombongan turun dari golfcar persis di depan smelter baru yang akan
                        diresmikan. Sepanjang jalan menuju lokasi ini, puluhan karyawan PTS menyambutnya dengan berjajar
                        di sisi jalan dengan bendera merah putih kecil.

                        Sementara itu, di samping bangunan smelter baru didirikan tenda besar untuk upacara peresmian.
                        Chief Executive Officer Mitsubishi Materials Corporation (MMC), Naoki Ono (Ono) secara khusus
                        datang dari Tokyo Jepang, dan Chairman Freeport McMoran (FCX), Mr. Richard Adkerson dari Amerika
                        Serikat semata-mata untuk menghadiri acara tersebut.

                        Ada empat petinggi MMC yang datang langsung dari Tokyo selain Mr. Ono. Mereka adalah Managing
                        Executive Officer & President Metals Company MMC Katsuyoshi Isaji, General Manager Metallurgy
                        Division Metals Company MMC Teruyuki Matsutani, General Manager Planning & Administration
                        Department Metals Company MMC Munenari Narazaki, dan AGM Planning & Administration Department
                        Metals Company MMC Atsushi Todo.
                        <br>
                        <br>
                        Presiden Jokowi dan rombongan berhenti di depan smelter baru untuk mendapatkan penjelasan
                        mengenai operasional pengolah katoda tembaga tersebut dari Mr. Sato. Mr. Sato menjelaskannya
                        dengan menggunakan bahasa Indonesia. Penjelasannya juga bisa didengarkan melalui sound system di
                        lokasi acara.

                        Setelah itu, Presiden Jokowi menyempatkan diri untuk meninjau smelter yang menggunakan teknologi
                        ramah lingkungan dari MMC ini. Ia sempat menaiki tangga pabrik yang ada di samping tenda acara.
                        Ia didampingi sejumlah paspampres dan para menteri. Tampak juga Gubernur Jatim Khofifah Indar
                        Parawansa.

                        Peninjauan pabrik secara langsung itu berlangsung sekitar setengah jam. Tidak diketahui apa saja
                        yang ditanyakan Presiden Jokowi kepada para top manajemen PTS ini. Yang pasti, usai selesai
                        peninjauan smelter, Presiden Jokowi tampak sumringah saat berjalan di bawah terik matahari
                        menuju tempat acara peresmian.
                        <br>
                        <br>
                        Ekspansi keempat smelter PTS ini akan menambah kapasitas produksi dari 1 juta ton konsentrat
                        menjadi 1,3 juta ton. Investasi untuk ekspansi smelter baru ini memang dibiayai PTFI.


                        Setelah peninjauan selesai, upacara peresmian ekspansi keempat PTS diawali dengan sambutan
                        Menteri BUMN Erick Thohir (Erick). Dikatakan bahwa peresmian ini menjadi bukti keseriusan
                        pemerintah dalam menjalankan hilirisasi.

                        "Meskipun ada pihak yang tidak paham akan urgensi kebijakan hilirisasi, namun tindakan yang
                        telah diambil Bapak Presiden Jokowi adalah langkah yang tepat dan kenyataan yang harus terjadi,"
                        katanya.

                        Erick menambahkan, Indonesia merupakan negara besar dengan sumber daya mineral yang melimpah.
                        Kebijakan hilirisasi menjadi sebuah komitmen agar tidak lagi diperalat oleh bangsa lain.

                        Menurut menteri yang juga Ketua Umum PSSI ini, Indonesia tidak bisa secara terus-menerus menjual
                        mineral mentah tanpa ada manfaat berkelanjutan, seperti penciptaan lapangan kerja maupun
                        transfer teknologi.
                        <br>
                        <br>
                        "Kalau ada pihak yang memprotes hilirisasi dan ingin kita terus-menerus menjual bahan mineral
                        mentah, saya rasa perlu dipertanyakan nasionalismenya," kata Erick.

                        Perluasan pabrik PTS ini tak hanya bermakna bagi FCX dan MMC, tapi juga pemerintah Indonesia.
                        Presiden Jokowi dalam sambutannya mengapresiasi ekspansi PTS yang dapat meningkatkan kapasitas
                        produksi anoda dan katoda tembaga serta nilai tambah di Indonesia.

                        ‚ÄúProduksi anoda maupun katoda tembaga naik dari 1 juta ton per tahun menjadi 1,3 juta ton per
                        tahun. Ini menunjukkan komitmen PTFI yang bekerjasama dengan MMC untuk menghilirkan, membangun
                        hilirisasi dengan ekspansi sehingga nilai tambah itu ada di Indonesia,‚Äù kata Presiden Jokowi.

                        Presiden Jokowi berharap, ekspansi PTS akan menambah kapasitas produksi. Dengan semua itu,
                        Indonesia akan mendapatkan nilai tambah.
                        
                        Presiden Jokowi mendorong agar hilirisasi tidak hanya dilakukan pada bahan-bahan mineral, tetapi
                        juga dalam komoditas perkebunan dan pertanian. Menurut Presiden Jokowi, hilirisasi tersebut
                        dapat meningkatkan kesempatan kerja dan nilai tambah di Indonesia.

                        "Semuanya tentang hilirisasi memberi nilai tambah di negara kita, kesempatan kerja itu ada di
                        negara kita, baik itu di perkebunan, pertanian, perikanan, semuanya. Jangan sampai kita kirim
                        lagi mineral dalam bentuk mentah, bahan-bahan perkebunan, komoditas perkebunan, pertanian dalam
                        bentuk mentah," tegas Presiden Jokowi.
                        <br>
                        <br>
                        Usai upacara, Presiden Jokowi masih menyempatkan diri berbicara dengan para wartawan di luar
                        tenda. Pada saat itu, ia menegaskan bahwa upaya hilirisasi dalam setiap komoditas di Indonesia
                        yang bertujuan untuk meningkatkan nilai tambah guna memperkuat pertumbuhan ekonomi nasional.

                        ‚ÄúSekali lagi, nilai tambah dari ekspansi tersebut akan menjadikan negara ini menjadi negara
                        produsen, menjadi negara produksi, dan kekuatan pertumbuhan ekonomi itu ada di produksi, bukan
                        di konsumsi,‚Äù kata Presiden Jokowi.

                        Tak hanya melayani para wartawan, Presiden Jokowi juga sempat memenuhi permintaan para karyawan
                        PTS untuk foto bersama. Ini membuat terkesan para karyawan. Mereka juga punya foto bersama
                        Presiden Jokowidan para pimpinan PTS yang juga foto bersama usai peresmian.

                        Sungguh, ini akhir tahun bersejarah bagi PTS dan seluruh pimpinan serta karyawannya. (Mitha dan
                        Tim SM)

                    </p>
