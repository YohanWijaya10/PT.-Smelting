                    <p>
                        PT Smelting (PTS) continues to campaign for a healthy lifestyle among all its employees. At the end
                        of 2023, PTS once again held promotive and preventive activities to prevent the spread of HIV/AIDS.
                        The agenda carried the theme "Let Communities Lead."
    
                        The activity started with a site rally to various areas in PTS such as Workshop, Warehouse, in front
                        of the Smelter lift, in front of the Refinery gate, Laboratory entrance, Logistics driver waiting room,
                        Jetty, Laydown, New Acid Plant project, and the mosque. Also, the main contractor's office, truck
                        parking, security post gate, contractor's canteen, gas station crossing, in front of Utility, Ume
                        Sembada office, and changing room.
    
                        Officers distributed informational pamphlets related to HIV/AIDS during the site rally. They also had
                        brief discussions with workers they encountered in those areas. The site rally activity took place on
                        Tuesday, December 19, 2023, from 13:30 to 15:00 WIB. Despite the scorching heat, the officers were
                        very enthusiastic about this activity.
    
                        After the site rally, questionnaires were distributed. The aim was to understand the workers'
                        knowledge of HIV/AIDS, their attitudes when family members or colleagues suffer from HIV/AIDS, and
                        workers' behavior towards the risk of contracting HIV/AIDS. The questionnaire was anonymous and
                        distributed randomly to some workers starting from December 20, 2023. Questionnaires were also
                        distributed during the HIV/AIDS seminar.
    
                        <br>
                        <br>
                        The last activity in the series of events commemorating World AIDS Day in 2023 was a seminar. Unlike
                        the previous year, this seminar was held face-to-face on December 27, 2023, from 13:30 to 15:30 WIB.
                        There were 77 seminar participants, consisting of PTS employees and contractors. This seminar invited
                        speakers from PKBI East Java (Indonesian Family Planning Association). The speakers shared knowledge
                        about HIV/AIDS, its transmission and prevention, as well as their experiences in prevention efforts
                        in the Gresik and Surabaya areas.
    
                        During the seminar, preventive measures and the risk of HIV/AIDS transmission were campaigned. The
                        speakers also encouraged participants to voluntarily undergo testing through VCT (Voluntary Counseling
                        Test) if they were at risk. Early detection of HIV/AIDS can prevent further transmission and reduce
                        the severity of HIV/AIDS as patients can be promptly treated to continue living a normal life.
    
                        "This HIV/AIDS Seminar was quite interesting because it opened insights into HIV/AIDS. The speakers
                        shared their experiences directly with people infected with HIV, making it more impactful. A
                        suggestion for future seminar activities is to add speakers from the religious side as one of the
                        fortifications," said Andika, Maintenance Utility, one of the seminar participants. (Kartikadsp/SM)
