                    <h3>10. STABILITY AND REACTIVITY</h3>
                    <div class="isimsds" style="margin-left:30px;">
                        <p><strong>Stability</strong>
                        </p>
                        <p>Copper is stable and not considered reactive under normal temperatures and pressures</p>
                        <p>&nbsp;</p>
                        <p><strong>Condition to Avoid</strong> </p>
                        <p>Shock sensitive compounds are formed with acetylenic compounds, ethylene oxide or azide
                            compounds. Copper in finely-divided powder or granular form reacts with strong oxidants like
                            chlorates, bromates, iodates and ammonium nitrate causing a potential explosion hazard.</p>
                        <p>&nbsp;</p>
                        <strong>Incompatible Materials</strong>
                        <p>Copper is incompatible with nitric acid, acetylene, chlorine fluorine, chlorine trifluoride,
                            and peroxides.</p>
                        <p>&nbsp;</p>
                        <strong>Hazardous Decomposition</strong>
                        <p>High temperature operations such as oxy-acetylene cutting, electric arc welding, arc-air
                            gouging or overheating a molten metal bath may generate fumes. The fumes will contain copper
                            oxides, which, on inhalation in sufficient quantity, can produce metal fume fever.</p>
                        <p>&nbsp;</p>
                        <p><strong>Hazardous Polymerization</strong> </p>
                        <p>Hazardous polymerization or runaway reactions will not occur.</p>
                    </div>
                </div>

                <div class="itemmsds">
                    <p><strong> 11. TOXICOLOGICAL INFORMATION </strong></p>
                    <div class="isimsds" style="margin-left:30px; font-family: Verdana, Geneva, sans-serif;">
                        <strong>General: </strong>
                        <p>Copper is an essential element, but can become toxic when inhaled or ingested in large doses.
                            Most reports of acute toxicity are from suicidal attempts involving ingestion of copper
                            sulphate. Individuals with a rare disorder called “Wilson’s Disease” (estimated prevalence
                            0.003% of the population) are predisposed to accumulate copper and should not be
                            occupationally exposed. However, in the form in which this product is sold it is relatively
                            non-toxic. The major route of exposure would be through the generation and inhalation of
                            copper oxide fume.</p>
                        <p>&nbsp;</p>
                        <strong>Skin: </strong>
                        <p>Copper metal is not irritating to skin other than by direct abrasive action of metal
                            particles on skin tissue. </p>
                        <p>&nbsp;</p>
                        <strong>Eye: </strong>
                        <p>Contact with dust or fume may cause local irritation. Embedded copper particles in the eye
                            result in a brownish or green-brown discoloration of the cornea, lens and iris (chalcosis)
                            which may progress to serious ocular complications. </p>
                        <p>&nbsp;</p>
                        <strong>Inhalation: </strong>
                        <p>An intense, short-term exposure to fumes from cutting or welding, etc. could result in the
                            condition called metal fume fever. The symptoms of metal fume fever generally occur within 3
                            to 10 hours. They may include immediate dryness and irritation of the throat, metallic
                            taste, tightness of the chest, and coughing that may later be followed by flu-like symptoms
                            of fever, malaise, perspiration, frontal headache, muscle cramps, low back pain,
                            occasionally blurred vision, nausea, and vomiting. Severe cases could cause pulmonary
                            congestion and edema as well as acute encephalopathy with possible seizures, coma, and
                            death. However, short term exposures of this magnitude are unlikely in industry today. Those
                            experiencing a single acute episode of metal fume fever generally recover slowly but without
                            apparent residual effects. Copper dust may be irritating to the nasal passages and the
                            throat with a sweetish, metallic taste and excessive salivation. </p>
                        <p>&nbsp;</p>
                        <strong>Ingestion: </strong>
                        <p>Individuals reported to have ingested large quantities of copper salts have reported
                            gastrointestinal effects including vomiting, diarrhea, nausea, malaise, anorexia, abdominal
                            pain and a metallic taste in the mouth. Effects on the kidneys and liver, and even death
                            have also been reported in severe cases of copper poisoning. However, copper is a strong
                            emetic and spontaneous vomiting following ingestion usually limits uptake of copper.</p>
                        <p>&nbsp;</p>
                        <strong>Chronic: </strong>
                        <p>Prolonged exposure to copper dust or fume can cause irritation to the eye and skin. A green
                            discoloration of the skin has been reported in some copper workers similar to that sometimes
                            experienced from copper jewelry. This green discoloration may occasionally also be seen in
                            hair (particularly note able in blonde hair), nails and teeth. A few instances of allergic
                            contact dermatitis have been reported. Copper is not listed as a human carcinogen by the
                            Occupational Safety and Health Administration (OSHA), the National Toxicology Program (NTP),
                            the International Agency for Research on Cancer (IARC), the American Conference of
                            Governmental Industrial Hygienists (ACGIH) or the European Union (EU).</p>
                    </div>
                </div>

                <div class="itemmsds">
                    <h3>12. ECOLOGICAL INFORMATION</h3>
                    <div class="isimsds" style="margin-left:30px;">
                        <p>Copper metal is insoluble in water and generally has essentially no direct bioavailability.
                            However, its processing or extended exposure in the aquatic and terrestrial environments can
                            lead to the release of copper in bio available forms. These can cause detrimental
                            environmental effects. The mobility of copper in soluble forms is media dependent. These can
                            bind with inorganic and organic ligands and particulates, reducing mobility and
                            bioavailability in soil and water. Bioavailability is also controlled by other factors such
                            as pH and hardness in the aquatic environment.</p>
                    </div>
