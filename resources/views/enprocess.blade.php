                <h4 class="heading fw-bold foregreen">1. Smelting Furnace (S-Furnace)<span class="fw-lighter"></span>
                </h4>
                <p class="mb-5">Dried concentrate and flux materials like silica sand are injected into S-furnace through vertical lances and oxidized with oxygen-enriched air to produce molten matte and slag, and their mixture overflows from the S-furnace down to the CL-furnace through launder.
                </p>

            </div>
        </div>
    </div>
    <div class="container">
        <div class="row justify-content-center mb-5 mt-3">
            <div class="col-md-3   imageprocess">
