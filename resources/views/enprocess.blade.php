            </div>
            <div class="col">
                <h4 class="heading fw-bold foregreen">3. Converter (C-Furnace)<span class="fw-lighter"></span></h4>
                <p class="mb-5">Matte and flux (limestone) are reacted with oxygen-enriched air to form blister copper and then separated from the slag by difference of specific gravity. Slag (Cu14%) is returned to S-furnace, and blister copper is siphoned out and forwarded to Anode furnaces.</p>
            </div>
        </div>
    </div>
    <div class="container">
        <div class="row justify-content-center mb-5 mt-3">
