            </div>
            <div class="col">
                <h4 class="heading fw-bold foregreen">2. Slag Cleaning Furnace (CL-Furnace)<span
                        class="fw-lighter"></span></h4>
                <p class="mb-5">CL-furnace is heated by two sets of delta type electrode (2100 &1500 KVA). Matte is separated from slag by difference of specific gravity. Slag is overflowed, water-granulated and sold to cement industry while molten matte (Cu 68%) is constantly siphoned out and transferred to the C-furnace through launder.</p>

            </div>
        </div>
    </div>
    <div class="container">
        <div class="row justify-content-center mb-5 mt-3">
            <div class="col-md-3   imageprocess">
