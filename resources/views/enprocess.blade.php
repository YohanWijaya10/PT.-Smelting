                <h4 class="heading fw-bold foregreen">4. Anode Furnace<span class="fw-lighter"></span></h4>
                <p class="mb-5">Blister copper from C-furnace is delivered into one of Anode furnaces by using switching launder system. In this furnace, oxidation and reduction reactions are occurred to produce refined copper ready for casting.</p>
            </div>

        </div>
    </div>
